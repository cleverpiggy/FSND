def look_some_things_up():
    #Band name, venue name, show time of future shows
    now = datetime.now()
    query = db.session.query(Show.start_time, Artist.name, Venue.name).join(Artist).join(Venue).filter(Show.start_time > now)
    print (query.statement.compile())
    all_ = query.all()
    for d, an, vn in all_:
        print (format_datetime(d.isoformat()), an, vn, sep=' :: ')

    # # #count a particular artist's shows
    print()
    query2 = db.session.query(Artist.name).filter(Artist.name=="The Wild Sax Band").join(Show)
    print (query2.statement.compile())
    print (query2.count())

    # #Artist name, num shows, ordered by num shows
    print()
    query3 = db.session.query(Artist.name, func.count()).join(Show).group_by(Artist.name).order_by(func.count())
    print (query3.statement.compile())
    for a, b in query3.all():
        print(a, b)

    #Venues ordered by state -- then order by city
    print()
    query4 = db.session.query(Venue.name, Venue.city, Venue.state, Venue.id).order_by(Venue.state, Venue.city)
    print (query4.statement.compile())
    for a, b, c, d in query4.all():
        print (a, b, c, d)

    #Venue name, num_future_shows
    print()
    stmt = db.session.query(Show).filter(Show.start_time > now).subquery()
    query5 = db.session.query(Venue.name, func.count(stmt.c.start_time).label('num_future_shows')).outerjoin(stmt).group_by(Venue.id)
    print (query5.statement.compile())
    for r in query5.all():
        print(r)

    print()
