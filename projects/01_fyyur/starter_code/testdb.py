def populate(model, funk):
    try:
        db.session.add_all(model(**d) for d in funk())
        db.session.commit()
        print (model.__name__, ' populated')

    except:
        print(model.__name__, sys.exc_info())
        db.session.rollback()
    finally:
        db.session.close()


